	<h1>DMCA takedown request requirements</h1>
	<p>
		We take the intellectual property rights of others seriously and require that our users do the
		same. The Digital Millennium Copyright Act (DMCA) established a process for addressing claims of
		copyright infringement. If you own a copyright or have authority to act on behalf of a copyright
		owner and want to report a claim that a third party is infringing that material on or through
		GitLab's services, please submit a DMCA report via Discord or email and we will take appropriate
		action.
	</p>
	<br />
	<h2 class="mt-0 text-3xl">DMCA Report Requirements</h2>
	<ul class="list-disc">
		<li>A description of the copyrighted work that you claim is being infringed;</li>
		<li>
			A description of the material you claim is infringing and that you want removed or access to
			which you want disabled with a URL and proof you are the original owner or other location of
			that material;
		</li>
		<li>Your name, title (if acting as an agent), address, telephone number, and email address;</li>
		<li>
			The following statement: <i
				>"I have a good faith belief that the use of the copyrighted material I am complaining of is
				not authorized by the copyright owner, its agent, or the law (e.g., as a fair use)";</i
			>
		</li>
		<li>
			The following statement: <i
				>"The information in this notice is accurate and, under penalty of perjury, I am the owner,
				or authorized to act on behalf of the owner, of the copyright or of an exclusive right that
				is allegedly infringed";</i
			>
		</li>
		<li>
			The following statement: <i
				>"I understand that I am subject to legal action upon submitting a DMCA request without
				solid proof.";</i
			>
		</li>
		<li>
			An electronic or physical signature of the owner of the copyright or a person authorized to
			act on the owner's behalf.
		</li>
	</ul>
	<div>
		<h4>Your DMCA take down request should be submit via the following links:</h4>
